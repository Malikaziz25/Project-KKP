    private void loadData(String keys){
      Object[] Baris ={"ID Absensi","ID Pegawai","Nama Pegawai", "Tanggal", "Jam Masuk","Jam Pulang","Status","Keterangan"};
      DefaultTableModel tabmode = new DefaultTableModel (null, Baris);
      table.setModel(tabmode);
      table.setRowHeight(40);
      String cari = "";
      String sql = "";
      if(keys != ""){
          cari = " WHERE pegawai.nip LIKE '%"+keys+"%' OR pegawainamapegawai LIKE '%"+keys+"%' ";
      }
      sql = "SELECT absensi.*,pegawai.namapegawai FROM absensi INNER JOIN pegawai ON absensi.idPegawai = pegawai.idpegawai";      
      try {
          java.sql.Connection conn = database.Database.getConn();
          java.sql.Statement stat = conn.createStatement();
          ResultSet hasil = stat.executeQuery(sql+cari);
          int index = 0;
          while(hasil.next()){
              SimpleDateFormat sdf = new SimpleDateFormat("dd MMMM yyyy", new Locale("id","ID"));
                java.util.Date date = new java.util.Date(hasil.getDate(3).getTime());
                String tgl = sdf.format(date);
              String a = hasil.getString(1);
              String b = hasil.getString(2);
              String d = hasil.getString(4);
              String e = hasil.getString(5);
              String f = hasil.getString(6);
              String g = hasil.getString(7);
              String h = hasil.getString(8);

              Object[] data={a,b,h,tgl,d,e,f,g};
              tabmode.addRow(data);
          }
      }catch(SQLException e){   
          e.printStackTrace();
      }
         
        
    }
    
