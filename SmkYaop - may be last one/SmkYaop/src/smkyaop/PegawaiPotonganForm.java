    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = getRow();
        if(row < 0){
            Helper.alert("PIlih data dahulu di tabel!");
            return;
        }
        String id = tfId.getText();
        
        int tanya = Helper.tanya("Apakah anda ingin Mengupdate data ini ?");
        if(tanya == JOptionPane.YES_OPTION && !id.isEmpty() ){
            String idpot = tfIdPot.getText();
            String pegawai = tfIdPeg.getText();
            java.util.Date tgl = tfTgl.getDate();
            String keterangan = taket.getText();


            if(idpot.isEmpty()){
                Helper.alert("Isi potongan dulu!");
                return;
            }
            if(pegawai.isEmpty() ){
                Helper.alert("Isi pegawai benar!");
                return;
            }
            if(keterangan.isEmpty() ){
                Helper.alert("Isi keterangan dulu!!");
                return;
            }

            if(tgl == null ){
                Helper.alert("Isi tgl benar!");
                return;
            }        
            String query = "UPDATE potonganpegawai SET idpegawai = ? ,tanggal = ?,idpotongan = ?,keterangan=? WHERE id = ?";
            try{
                Connection conn = Database.getConn();
                PreparedStatement ps = conn.prepareStatement(query);
                ps.setInt(1,Integer.parseInt(pegawai));
                ps.setDate(2, new java.sql.Date(tgl.getTime()));
                ps.setInt(3,Integer.parseInt(idpot));           
                ps.setString(4,keterangan);
                ps.setInt(5,Integer.parseInt(id));
                int result = ps.executeUpdate();
                if(result > 0 ){
                    Helper.alert("Data Berhasil diupdate!");                
                }
            }catch(SQLException ex){
                ex.printStackTrace();
            }
        }
        loadData("");
        clear();
        firetable();
        active(true);
    }//GEN-LAST:event_btnEditActionPerformed

