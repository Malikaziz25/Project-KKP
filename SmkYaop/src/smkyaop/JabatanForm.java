    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int row = getRow();
        if(row >= 0 ){
            firetable();
        }
        
        String nama = tfNama.getText();
        int index = comboJenis.getSelectedIndex();
        String tunjangan = tfTunjangan.getText();
        String gapok = tfGapok.getText();
        String jenis = "";
        if(nama.isEmpty()){
            Helper.alert("Isi nama jabatan dulu!");
            return;
        }
        if(tunjangan.isEmpty() || Integer.parseInt(tunjangan) < 0  ){
            Helper.alert("Isi Tunjangan dengan benar!");
            return;
        }
        if(index == 0){
            Helper.alert("Pilih Jenis Jabatan dahulu!");
            return;
        }        
        
        jenis = comboJenis.getSelectedItem().toString();
        String query = "INSERT INTO jabatan(namajabatan,jenisjabatan,tunjanganJabatan,gapok) VALUES(?,?,?,?)";
        try{
            Connection conn = Database.getConn();
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, nama);
            ps.setString(2, jenis);
            ps.setInt(3, Integer.parseInt(tunjangan));
            ps.setInt(4,Integer.parseInt(gapok));
            int result = ps.executeUpdate();
            if(result > 0 ){
                Helper.alert("Data Berhasil dimasukkan!");                
            }
        }catch(SQLException ex){
            ex.printStackTrace();
        }
        loadData("");
        clear();
        firetable();
       tfGapok.setEnabled(false);
    }//GEN-LAST:event_btnSaveActionPerformed

