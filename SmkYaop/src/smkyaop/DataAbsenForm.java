    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if(row < 0){
            Helper.alert("PIlih data dahulu di tabel!");
            return;
        }
        String id = tfId.getText();

        int tanya = Helper.tanya("Apakah anda ingin Mengupdate data ini ?");
        if(tanya == JOptionPane.YES_OPTION && !id.isEmpty() ){

            String idp = tfId2.getText();
            String masuk = tfMasuk.getText();
            String pulang = tfPulang.getText();
            java.util.Date tgl = tfTanggal.getDate();
            String keterangan = "";
            int index = combo.getSelectedIndex();
            keterangan = combo.getSelectedItem().toString();
            if(idp.isEmpty()){
                Helper.alert("Isi pegawai dulu!");
                return;
            }

            if(tgl == null){
                Helper.alert("Masukkan tanggal!");
                return;
            } 

            if(index == 0){
                Helper.alert("Pilih Keterangan dahulu!");
                return;
            }
            java.util.Date masuktime = null,pulangtime=null;
            if(!masuk.isEmpty()){
                try {
                     masuktime = new SimpleDateFormat("HH:mm").parse(masuk);              
                } catch (ParseException ex) {
                   Helper.alert("masukkan jam masuk dengan benar!");
                   return;
                }
            }

            if(!pulang.isEmpty()){
                try {
                     pulangtime = new SimpleDateFormat("HH:mm").parse(pulang);                 
                } catch (ParseException ex) {
                   Helper.alert("masukkan jam pulang dengan benar!");
                   return;
                }
            }


            String query = "UPDATE absensi SET idpegawai = ?,tanggal=?,jamMasuk=?,jamPulang=?,status=?,keterangan=? WHERE idAbsensi = ?";
            try{
                Connection conn = Database.getConn();
                PreparedStatement ps = conn.prepareStatement(query);
                ps.setInt(1, Integer.parseInt(idp));
                ps.setDate(2, new java.sql.Date(tgl.getTime()));
                ps.setTime(3,new java.sql.Time(masuktime.getTime()));
                ps.setTime(4,new java.sql.Time(pulangtime.getTime()));            
                ps.setInt(5,index);
                ps.setString(6, keterangan);
                ps.setInt(7,Integer.parseInt(id));
                int result = ps.executeUpdate();
                if(result > 0 ){
                    Helper.alert("Data Berhasil diUpdate!");
                }
            }catch(SQLException ex){
                ex.printStackTrace();
            }
        }
        loadData("");
        clear();
        firetable();

    }//GEN-LAST:event_btnEditActionPerformed

