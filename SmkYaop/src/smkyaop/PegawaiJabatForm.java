    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        active(false);
        int row = table.getSelectedRow();
        String id = table.getValueAt(row, 0).toString();
        String idp = table.getValueAt(row, 1).toString();
        String namap = table.getValueAt(row, 2).toString();
        String tglMasuk = table.getValueAt(row, 3).toString();
        String tglKeluar = table.getValueAt(row, 4).toString();        
        tfId.setText(id);
        tfId2.setText(idp);
        java.util.Date tgl1 = null;
        java.util.Date tgl2 = null; 
        SimpleDateFormat sdf = new SimpleDateFormat("dd MMMM yyyy", new Locale("id","ID"));        
        try{
            tgl1 = sdf.parse(tglMasuk);
            if(!tglKeluar.equals("-")){
                tgl2 = sdf.parse(tglKeluar);                        
            }
            tfTglMasuk.setDate(tgl1);
            tfTglKeluar.setDate(tgl2);
        }catch(ParseException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_tableMouseClicked

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if(row < 0){
            Helper.alert("PIlih data dahulu di tabel!");
            return;
        }
        String id = tfId.getText();
        
        int tanya = Helper.tanya("Apakah anda ingin Mengupdate data ini ?");
        if(tanya == JOptionPane.YES_OPTION && !id.isEmpty() ){
            String idpegawai = tfId2.getText();
           
            java.util.Date tglMasuk = tfTglMasuk.getDate();
            java.util.Date tglKeluar = tfTglKeluar.getDate();        
            String jenis = "";
             if(id.isEmpty()){
                Helper.alert("Isi id dulu!");
                return;
            }
            if(idpegawai.isEmpty()){
                Helper.alert("Isi Pegawai dulu!");
                return;
            }

            if(tglMasuk == null){
                Helper.alert("Isi Tanggal masuk  dulu");
                return;        
            }
            
        LocalDate local = tglMasuk.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate nowl = LocalDate.now();
        
        long year = java.time.temporal.ChronoUnit.DAYS.between(local, nowl);
        // hitung jumlah tanggal berdasarkan tahun kabisat atau tidak
        int hariTahunini = (Year.of(Year.now().getValue()).isLeap()) ? 366 : 365;
        
        long tahunJabat = year/hariTahunini;            

            String query = "UPDATE pegawaijabat SET mulai = ?,keluar=?,idPegawai=?, masajabat = ? WHERE id = ?";
            try{
                Connection conn = Database.getConn();
                PreparedStatement ps = conn.prepareStatement(query);
                ps.setDate(1, new java.sql.Date(tglMasuk.getTime()));
                if(tglKeluar == null){
                    ps.setDate(2, null);
                }else{
                    ps.setDate(2, new java.sql.Date(tglMasuk.getTime()));
                }
                
                ps.setInt(3, Integer.parseInt(idpegawai));
                ps.setInt(4,(int)tahunJabat);
                ps.setInt(5, Integer.parseInt(id));
                int result = ps.executeUpdate();
                if(result > 0 ){
                    Helper.alert("Data Berhasil dimasukkan!");                
                }
            }catch(SQLException ex){
                ex.printStackTrace();
            }

        }
        loadData("");
        clear();
        firetable();
        active(true);
    }//GEN-LAST:event_btnEditActionPerformed

