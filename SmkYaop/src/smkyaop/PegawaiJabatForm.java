    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if(row >= 0 ){
            firetable();
        }
        
        
        
        String idpegawai = tfId2.getText();
        java.util.Date tglMasuk = tfTglMasuk.getDate();
        java.util.Date tglKeluar = tfTglKeluar.getDate();        
        String jenis = "";
        if(idpegawai.isEmpty()){
            Helper.alert("Isi Pegawai dulu!");
            return;
        }
        
        if(tglMasuk == null){
            Helper.alert("Isi Tanggal masuk  dulu");
            return;        
        }
        
        // menghitung berapa hari udah lewat dari ttgl a ke tanggal b       
        LocalDate local = tglMasuk.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate nowl = LocalDate.now();
        
        long year = java.time.temporal.ChronoUnit.DAYS.between(local, nowl);
        // hitung jumlah tanggal berdasarkan tahun kabisat atau tidak
        
        int hariTahunini = (Year.of(Year.now().getValue()).isLeap()) ? 366 : 365;
        
        long tahunJabat = year/hariTahunini;

        String query = "INSERT INTO pegawaijabat(mulai,keluar,idPegawai,masajabat) VALUES(?,?,?,?)";
        try{
            Connection conn = Database.getConn();
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setDate(1, new java.sql.Date(tglMasuk.getTime()));
            if(tglKeluar == null){
                ps.setDate(2, null);
            }else{
                ps.setDate(2, new java.sql.Date(tglMasuk.getTime()));
            }
                ps.setInt(3, Integer.parseInt(idpegawai));
                ps.setInt(4, (int)tahunJabat);
                
            int result = ps.executeUpdate();
            if(result > 0 ){
                Helper.alert("Data Berhasil dimasukkan!");                
            }
        }catch(SQLException ex){
            ex.printStackTrace();
        }
        loadData("");
        clear();
        firetable();
    }//GEN-LAST:event_btnSaveActionPerformed

