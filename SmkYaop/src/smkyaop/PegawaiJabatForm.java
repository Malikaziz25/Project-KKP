    private void loadData(String keys){
        String header[] = {"ID","ID Pegawai","Nama Pegawai","Tanggal Masuk","Tanggal Keluar","Masa Kerja"};
        DefaultTableModel dtm = new DefaultTableModel(null,header);
        table.setModel(dtm);
        table.setRowHeight(40);
        table.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
          DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer();
                    table.getColumnModel().getColumn(0).setPreferredWidth(5); 
                    table.getColumnModel().getColumn(1).setPreferredWidth(5); 
          cellRenderer.setHorizontalAlignment(JLabel.CENTER);
          table.getColumnModel().getColumn(0).setCellRenderer(cellRenderer); 
          table.getColumnModel().getColumn(1).setCellRenderer(cellRenderer);       
          
         String query = "SELECT pegawaijabat.*,pegawai.idpegawai as idp , pegawai.namapegawai as namap FROM pegawaijabat INNER JOIN pegawai ON pegawaijabat.idPegawai = pegawai.idpegawai"; 
          DefaultTableCellRenderer renderer = (DefaultTableCellRenderer) table.getTableHeader().getDefaultRenderer();
          renderer.setHorizontalAlignment(0);                  
        String addQuery = "";
        if(keys != ""){
            addQuery = " WHERE p.namapegawai LIKE '%"+keys+"%' OR p.nip LIKE '%"+keys+"%' ";
        }
        try{            
            Connection conn = Database.getConn();
            PreparedStatement ps = conn.prepareStatement(query+addQuery);
            ResultSet rs = ps.executeQuery();            
            while(rs.next()){
                try{
                    SimpleDateFormat sdf = new SimpleDateFormat("dd MMMM yyyy", new Locale("id","ID"));
                    java.util.Date date = new java.util.Date(rs.getDate(2).getTime());
                    String tgl = sdf.format(date);
                    java.util.Date date2 = null;
                    String tgl2 = "-";
                    if(rs.getDate(3) != null){
                        date2 = new java.util.Date(rs.getDate(4).getTime());
                        tgl2 = sdf.format(date2);                                    
                    }
                    Object[] row = {rs.getString(1),rs.getString(4),rs.getString(6),tgl,tgl2,rs.getString("masajabat") +" Tahun" };
                    dtm.addRow(row);                    
                }catch(Exception ex1){                    
                    ex1.printStackTrace();
                }

            }
        }catch(SQLException ex){
            ex.printStackTrace();
        }
    }
    
