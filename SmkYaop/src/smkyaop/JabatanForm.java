    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        active(false);
        int row = getRow();
        tfId.setText(this.getFromTable(row)[0]);
        tfNama.setText(this.getFromTable(row)[1]);
        tfTunjangan.setText(this.getFromTable(row)[3]);
        comboJenis.setSelectedItem(this.getFromTable(row)[2]);
        tfGapok.setText(getFromTable(row)[4]);
        if(isFromPegawai == true && pegForm !=null){
            pegForm.setDataFromPegawai(getFromTable(row)[0]);
            isFromPegawai = false;
            pegForm = null;
            this.toBack();    
            dispose();
        }
    }//GEN-LAST:event_tableMouseClicked

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = getRow();
        if(row < 0){
            Helper.alert("PIlih data dahulu di tabel!");
            return;
        }
        String id = tfId.getText();
        
        int tanya = Helper.tanya("Apakah anda ingin Mengupdate data ini ?");
        if(tanya == JOptionPane.YES_OPTION && !id.isEmpty() ){
            String nama = tfNama.getText();
            int index = comboJenis.getSelectedIndex();
            String tunjangan = tfTunjangan.getText();
            String jenis = "";
            if(nama.isEmpty()){
                Helper.alert("Isi nama jabatan dulu!");
                return;
            }
            if(tunjangan.isEmpty() || Integer.parseInt(tunjangan) < 0){
                Helper.alert("Isi Tunjangan dengan benar!");
                return;
            }
            if(index == 0){
                Helper.alert("Pilih Jenis Jabatan dahulu!");
                return;
            }        

            jenis = comboJenis.getSelectedItem().toString();
            String gapok = tfGapok.getText();
            
            try{
                String query = "UPDATE jabatan SET namajabatan = ?,jenisjabatan = ?,tunjanganJabatan = ?, gapok = ? WHERE idjabatan = ?";
                PreparedStatement ps = Database.getConn().prepareStatement(query);
                ps.setString(1, nama);
                ps.setString(2, jenis);
                ps.setInt(3, Integer.parseInt(tunjangan));
                ps.setInt(4,Integer.parseInt(gapok));   
                ps.setInt(5, Integer.parseInt(id));
             
                int hasil = ps.executeUpdate();
                if(hasil > 0 ){
                    Helper.alert("Data Berhasil diUpdate!!");
                }
            }catch(SQLException ex){
                ex.printStackTrace();
            }
        }
        loadData("");
        clear();
        firetable();
        active(true);
      tfGapok.setEnabled(false);
    }//GEN-LAST:event_btnEditActionPerformed

