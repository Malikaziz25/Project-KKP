    private void loadData(String keys){
        String header[] = {"ID","ID Pegawai","Nama Pegawai","tanggal","ID Potongan","Nama Potongan","Nominal","Keterangan"};
        DefaultTableModel dtm = new DefaultTableModel(null,header);
        table.setModel(dtm);
        table.setRowHeight(40);
        table.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
          DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer();
                    table.getColumnModel().getColumn(0).setPreferredWidth(5); 
          cellRenderer.setHorizontalAlignment(JLabel.CENTER);
          table.getColumnModel().getColumn(0).setCellRenderer(cellRenderer);    
        String query = "SELECT potonganpegawai.*,potongan.id as idpot,potongan.namaPotongan as npot,potongan.nominal as nom,pegawai.namapegawai as np "
                + "FROM potonganpegawai,potongan,pegawai WHERE potonganpegawai.idpegawai = pegawai.idpegawai AND potonganpegawai.idpotongan = potongan.id ";
          DefaultTableCellRenderer renderer = (DefaultTableCellRenderer) table.getTableHeader().getDefaultRenderer();
          renderer.setHorizontalAlignment(0);                  
        String addQuery = "";
        if(keys != ""){
            addQuery = " AND pegawai.namapegawai LIKE '%"+keys+"%'";
        }
        try{            
            Connection conn = Database.getConn();
            PreparedStatement ps = conn.prepareStatement(query+addQuery);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
//        String header[] = {"ID","ID Pegawai","Nama Pegawai","tanggal","ID Potongan","Nama Potongan","Nominal","Keterangan"};
            String namapegawai = rs.getString("np");
                String tgl= new SimpleDateFormat("dd MMMMM yyyy",new Locale("id","ID")).format(rs.getDate(3));
                String nampot = rs.getString("npot");
                Object[] row = {rs.getString(1),rs.getString(2),namapegawai,tgl,rs.getString(4),nampot,rs.getString("nom"),rs.getString(5)};
                dtm.addRow(row);
            }
        }catch(SQLException ex){
            ex.printStackTrace();
        }
    }
    
